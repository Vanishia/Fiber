package com.bird.fiber.data.config

/**
 * 预览配置
 *
 * 集中管理预览相关的魔法数字
 */
object PreviewConfig {
    /**
     * 预览最大字符数
     *
     * 策略说明：
     * PreviewHelper 只负责"取前 N 字符"，不添加任何截断标记。
     * 视觉截断完全由 UI 层的 Text(maxLines=3, overflow=Ellipsis) 处理。
     * 足够长的预览确保无论单段还是多段内容，始终能填满 3 视觉行。
     *
     * 为什么是 500：
     * - 500 中文字符在手机屏幕上约 15-20 行，远超 3 行限制
     * - 确保任何类型的内容都能撑满 3 行预览区域
     * - 存储成本低廉（500 字符 ≈ 1-1.5KB），即使 10000 个文件也仅 ~15MB
     * - 无 I/O 开销（预览在同步时已计算好存入 Room DB）
     */
    const val MAX_CHARS = 500

    /**
     * 预览在 UI 中显示的最大行数
     *
     * 为什么是 3：
     * - 3 行足够看出笔记的大致内容
     * - 不会让列表卡片过高
     * - 与 maxLines 配合：预览文本足够长时自动折满 3 行
     */
    const val MAX_LINES = 3

    /**
     * 启动时预加载最近文件的预览数量
     *
     * 为什么是 30：
     * - 启动后立即加载最近 30 条到内存缓存
     * - 用户打开 App 立刻看到前 30 条有摘要
     * - 内存占用可控（30 条 × 200 字 ≈ 6KB）
     * - 不阻塞 UI（在后台加载）
     */
    const val RECENT_PREVIEW_LIMIT = 30
}
